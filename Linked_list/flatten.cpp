class Solution {
  public:
    // Function which returns the  root of the flattened linked list.
    Node* sortDown(Node* head,Node* tmp){
        if(tmp==NULL)return head;
        
        Node* tmp1=head;
        Node* tmp2=tmp;
        Node* dummy=new Node(-1);
        Node* ans=dummy;
        
        while(tmp1!=NULL && tmp2 !=NULL){
            if(tmp1->data > tmp2->data){
                dummy->bottom=tmp2;
                tmp2=tmp2->bottom;
                
            }
            else{
                dummy->bottom=tmp1;
                tmp1=tmp1->bottom;

            }
            dummy=dummy->bottom;
            
            if(tmp1==NULL&&tmp2!=NULL){
                dummy->bottom=tmp2;
            }
            else dummy->bottom=tmp1;
        }
        return sortDown(ans->bottom,tmp->next);
        
    }
    
    Node *flatten(Node *root) {
        if(root==NULL && root->next==NULL)return NULL;
        Node* head = root;
        Node* tmp = head->next;
        return sortDown(head,tmp);
        
    }
};


