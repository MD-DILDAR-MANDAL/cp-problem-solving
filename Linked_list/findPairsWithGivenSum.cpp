class Solution
{
public:
    vector<pair<int, int>> findPairsWithGivenSum(Node *head, int target)
    {
        vector<pair<int,int>>vp;
        pair<int,int>pr;
        Node* left = head;
        Node* right = head;
        
        while(right->next != NULL){
            right=right->next;
        }
        int sum=0;
        while(left->data < right->data){
            sum = left->data + right->data;
            
            if(sum < target){
                left = left->next;
            }
            else if(sum == target){
                vp.push_back(make_pair(left->data,right->data));
                left = left->next;
                right = right->prev;

            }
            else{
                right=right->prev;
            }
            
        }
        return vp;
    }
};

